package com.example.addaapplication;


public class DialogDataDetails  {


    private String name;
    private int nameSelected;

    public DialogDataDetails(String name) {
        this.name = name;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getNameSelected() {
        return nameSelected;
    }

    public void setNameSelected(int nameSelected) {
        this.nameSelected = nameSelected;
    }
}
